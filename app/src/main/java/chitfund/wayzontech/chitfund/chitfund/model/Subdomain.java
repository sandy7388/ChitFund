package chitfund.wayzontech.chitfund.chitfund.model;

public class Subdomain {
    private String strSubdomain, strUserId, strUsername, strRoleId, strName;

    public String getStrName() {
        return strName;
    }

    public void setStrName(String strName) {
        this.strName = strName;
    }

    public String getStrSubdomain() {
        return strSubdomain;
    }

    public void setStrSubdomain(String strSubdomain) {
        this.strSubdomain = strSubdomain;
    }

    public String getStrUserId() {
        return strUserId;
    }

    public void setStrUserId(String strUserId) {
        this.strUserId = strUserId;
    }

    public String getStrUsername() {
        return strUsername;
    }

    public void setStrUsername(String strUsername) {
        this.strUsername = strUsername;
    }

    public String getStrRoleId() {
        return strRoleId;
    }

    public void setStrRoleId(String strRoleId) {
        this.strRoleId = strRoleId;
    }
}
